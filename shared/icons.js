  svg(name, className = '') {
    const path = this.paths[name];
    if (!path) {
      console.warn(`SWT.Icons: "${name}" nicht gefunden`);
      return '';
    }
    const cls = className ? ` class="${className}"` : '';
    return `<svg viewBox="0 0 24 24"${cls}><path d="${path}"/></svg>`;
  },

  /**
   * SVG als DOM-Element
   */
  create(name) {
    const path = this.paths[name];
    if (!path) return null;
    const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
    svg.setAttribute('viewBox', '0 0 24 24');
    const pathEl = document.createElementNS('http://www.w3.org/2000/svg', 'path');
    pathEl.setAttribute('d', path);
    svg.appendChild(pathEl);
    return svg;
  },

  /**
   * Initialisiert alle data-icon Elemente im DOM
   * Aufruf: Am Ende des <body> nach Script-Load
   */
  init() {
    document.querySelectorAll('[data-icon]').forEach(el => {
      const name = el.dataset.icon;
      const svg = this.svg(name);
      if (svg && !el.querySelector('svg')) {
        el.innerHTML = svg;
      }
    });
  }
